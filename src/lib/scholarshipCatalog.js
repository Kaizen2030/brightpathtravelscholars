const SCHOLARSHIP_BY_SLUG = new Map(SCHOLARSHIP_CATALOG.map((item) => [item.slug, item]))
const SCHOLARSHIP_BY_TITLE = new Map(
  SCHOLARSHIP_CATALOG.flatMap((item) => [[item.title, item], ...((item.aliases ?? []).map((alias) => [alias, item]))]),
)

export function getScholarshipBySlug(slug) {
  return SCHOLARSHIP_BY_SLUG.get(slug) || null
}

export function getScholarshipByTitle(title) {
  return SCHOLARSHIP_BY_TITLE.get(title) || null
}

export function buildScholarshipDetailPath(slug) {
  return `/scholarships/${slug}`
}

export function buildScholarshipApplyPath(slug) {
  return `/scholarships/${slug}/apply`
}
